
num = [10, 20, 30, 40, 50]
print(num)

# iterate the list item using a while loop
iterate = 0
print("\nAfter Iterate: ")
while iterate < len(num):
    print(num[iterate])
    iterate += 1


# iteration the list item using for loop
print("\n\nIterate list itean suing for loop")
for i in num:
    print(i)




