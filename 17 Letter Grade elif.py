# Letter Grade

mark = int(input("Please enter your mark: "))

if mark >= 80:
    print("Grade: A+")
elif mark >= 70:
    print("Grade: A")
elif mark >= 60:
    print("Grade: -A")
elif mark >= 50:
    print("Grade: B")
elif mark >= 40:
    print("Grade: C")
elif mark >= 330:
    print("Grade: D")
else:
    print("Grade: F")


