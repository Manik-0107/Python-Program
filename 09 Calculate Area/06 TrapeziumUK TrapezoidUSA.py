# Calculate Trapezoid(US) / Trapezium (UK) area

a = float(input("\nPlease enter top base size: "))
b = float(input("Please enter bottom base size: "))
h = float(input("Please enter vertical height size: "))

area = 0.5*(a + b) * h

print("The Trapezoid area is: ", area,"\n")




